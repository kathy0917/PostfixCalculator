package SimpleCalculator;

public class MyCalculator {
	public int add(int x,int y){
	      return x + y;
	   }
	 public  int subtract(int x,int y){
	      return x - y;
	   }
	 public int multiply(int x,int y){
	      return x * y;
	   }
	 public  int divide(int x,int y){
	      return x / y;
	   }
	 public  int modulo(int x,int y){
	      return x % y;
	   }
}
